#Assignment Operators in Python
x=10
print(x)
#Assigning Multiple Values in same Line
v,b,c=100,2,4
print(v,b,c)
#Some Shorthand Operators
x+=20 #x=10 and 10 +20=30 Similar to x+20
print(x)
x-=2
print(x) #x=30 and 30-2=28 similar x-2
x*=2 #It uses 28 as x anot 10 because variable vales can be changed baed on assignments,28*2=56,Similar to x*2
print(x)
x/=2 #x=56 and 56/2=28.0,/ always returns result as float
print(x)

#Output
# 10
# 100 2 4
# 30
# 28
# 56
# 28.0