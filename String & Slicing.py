#String Basics and Slicing in Python
#Strings in Python Can Be Created in 3 Ways As Follows
s="Saikiran"
s1='Virat Kohli'
s2="""Rohit Sharma"""
print(s)
print(s1)
print(s2)
print(type(s))
print(s[6])
print(s[1:4])
print(s[:4])
print(s[0:])
print(s[::-1])
print(s[-5])

#Output
# Saikiran
# Virat Kohli
# "Rohit Sharma
# <class 'str'>
# a
# aik
# Saik
# Saikiran
# narikiaS
# k
