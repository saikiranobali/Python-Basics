#Different types of data types in python
my_integer = 42
my_float = 3.14
my_string = "Hello, World!"
my_boolean = True
my_list = [1, 2, 3, 4, 5]
my_tuple = (1, 2, 3, 4, 5)
my_dict = {'name': 'John', 'age': 30, 'city': 'New York'}
my_set = {1, 2, 3, 4, 5}
my_complex = 2 + 3j  # Complex number
print("Integer:", my_integer, "| Type:", type(my_integer))
print("Float:", my_float, "| Type:", type(my_float))
print("String:", my_string, "| Type:", type(my_string))
print("Boolean:", my_boolean, "| Type:", type(my_boolean))
print("List:", my_list, "| Type:", type(my_list))
print("Tuple:", my_tuple, "| Type:", type(my_tuple))
print("Dictionary:", my_dict, "| Type:", type(my_dict))
print("Set:", my_set, "| Type:", type(my_set))
print("Complex:", my_complex, "| Type:", type(my_complex))

#Output

# Integer: 42 | Type: <class 'int'>
# Float: 3.14 | Type: <class 'float'>
# String: Hello, World! | Type: <class 'str'>
# Boolean: True | Type: <class 'bool'>
# List: [1, 2, 3, 4, 5] | Type: <class 'list'>
# Tuple: (1, 2, 3, 4, 5) | Type: <class 'tuple'>
# Dictionary: {'name': 'John', 'age': 30, 'city': 'New York'} | Type: <class 'dict'>
# Set: {1, 2, 3, 4, 5} | Type: <class 'set'>
# Complex: (2+3j) | Type: <class 'complex'>